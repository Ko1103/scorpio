import UIKit.UIColor

extension UIColor {
    static var spDarkBlue: UIColor { return UIColor(red: 57/255, green: 62/255, blue: 70/255, alpha: 1) }
    static var spBlue: UIColor { return UIColor(red: 0, green: 173/255, blue: 181/255, alpha: 1) }
    static var spWhite: UIColor { return UIColor(red: 255, green: 244/255, blue: 224/255, alpha: 1) }
    static var spYellow: UIColor { return UIColor(red: 248/255, green: 181/255, blue: 0, alpha: 1) }
    static var spOrange: UIColor { return UIColor(red: 252/255, green: 60/255, blue: 60/255, alpha: 1) }
}
